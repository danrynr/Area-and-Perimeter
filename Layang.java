public class Layang implements BangunDatar {
    Layang() {}

    @Override
    public double luas(int s1, int s2) {
        return 0;
    }

    @Override
    public double luas1(int d1, int d2) {
        return (0.5 * d1 * d2);
    }

    @Override
    public double keliling(int s1, int s2) {
        return (2 * (s1 + s2));
    }
}
